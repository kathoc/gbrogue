def tile_bytes(rows: list[str]) -> list[int]:
    """Encode 8 rows of '*'/'.' into the GB 2bpp tile format.

    Each row contributes 2 bytes: low bitplane (bit 0 of the 2-bit color)
    and high bitplane (bit 1). We render glyphs at color 3 (both planes
    set) on color 0 (both planes clear) so palette swaps just swap fg/bg.
    """
    out: list[int] = []
    for row in rows:
        assert len(row) == 8, f"row width != 8: {row!r}"
        low = 0
        high = 0
        for i, ch in enumerate(row):
            if ch == "*":
                low |= 1 << (7 - i)
                high |= 1 << (7 - i)
        out.append(low)
        out.append(high)
    return out


# Full-width tiles kept in VRAM: map glyphs + monster letters + '@'.
# UI text is half-width (text4.c), so digits/lowercase 8x8 are dropped.
TILE8_SET = " .#-|+><^!?/=)]%*," + "@" + "".join(chr(c) for c in range(65, 91))


def main() -> None:
    OUT.parent.mkdir(parents=True, exist_ok=True)
    # --- pixel data: ROM bank 2 (loaded via a NONBANKED helper) ---
    lines: list[str] = []
    lines.append("/*")
    lines.append(" * GENERATED FILE — see scripts/gen_font.py")
    lines.append(" *")
    lines.append(" * Full-width (8x8) glyph tiles: map symbols, A..Z, '@'. 16 bytes")
    lines.append(" * per tile (GB 2bpp), color 3 on color 0. Lives in ROM bank 2;")
    lines.append(" * render_init() copies it to VRAM through a NONBANKED loader.")
    lines.append(" */")
    lines.append("#include <gb/gb.h>")
    lines.append("#include <stdint.h>")
    lines.append("")
    lines.append("#pragma bank 2")
    lines.append("")
    lines.append(f"#define TILES8_COUNT {len(TILE8_SET)}")
    lines.append("")
    lines.append("BANKREF(tiles_ascii_data)")
    lines.append("const uint8_t tiles_ascii_data[TILES8_COUNT * 16] = {")
    blank = ["........"] * 8
    for i, ch in enumerate(TILE8_SET):
        rows = GLYPHS.get(ch, blank)
        bs = tile_bytes(rows)
        hex_bytes = ", ".join(f"0x{b:02X}" for b in bs)
        lines.append(f"    /* {i:2d} {ch!r:<5}*/ {hex_bytes},")
    lines.append("};")
    lines.append("")
    OUT.write_text("\n".join(lines))

